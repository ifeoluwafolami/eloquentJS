function range(start, end) {
    let array = [];
    for (start; start <= end; start++) {
        array.push(start);
    }
    return array;
}

function sum(array) {
    numberSum = 0;
    for (number of array) {
        numberSum += number;
    }
    return numberSum;
}

console.log(sum(range(1, 10)));